    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
            ProductDTO productDTO= new ProductDTO();
            if (nametxt.getText().equals("") || costtxt.getText().equals("")
                || selltxt.getText().equals("") || brandtxt.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Please enter all the required details.");
            else {
                productDTO.setProdCode(codetxt.getText());
                productDTO.setProdName(nametxt.getText());
                java.util.Date utilDate = jDateChooser1.getDate();
                java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
                productDTO.setDate(sqlDate.toString());
                productDTO.setQuantity(Integer.parseInt(quantitytxt.getText()));
                productDTO.setCostPrice(Double.parseDouble(costtxt.getText()));
                productDTO.setSellPrice(Double.parseDouble(selltxt.getText()));
                productDTO.setBrand(brandtxt.getText());
            
                new ProductDAO().addProductDAO(productDTO);
                loadDataSet();
            }
    }//GEN-LAST:event_addBtnActionPerformed

