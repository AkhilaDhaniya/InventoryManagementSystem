    private void productTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productTableMouseClicked
        // TODO add your handling code here:
        int row = productTable.getSelectedRow();
        int col = productTable.getColumnCount();

        Object[] data = new Object[col];
        for (int i=0; i<col; i++)
            data[i] = productTable.getValueAt(row, i);

        codetxt.setText(data[0].toString());
        nametxt.setText(data[1].toString());
        costtxt.setText(data[2].toString());
        selltxt.setText(data[3].toString());
        brandtxt.setText(data[4].toString());
    }//GEN-LAST:event_productTableMouseClicked

