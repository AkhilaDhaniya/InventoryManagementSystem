    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        UserDTO userDTO=new UserDTO();
        if (nametext.getText().equals("") || usertext.getText().equals("")
                || passtext.getText().equals("") || usertype.getSelectedItem().equals(""))
            JOptionPane.showMessageDialog(null, "Please enter all the required details.");
            else {
                userDTO.setFullName(nametext.getText());
                userDTO.setLocation(loctext.getText());
                userDTO.setPhone(conttext.getText());
                userDTO.setUsername(usertext.getText());
                userDTO.setPassword(passtext.getText());
                userDTO.setUserType(usertype.getSelectedItem().toString());
            
                new UserDAO().addUserDAO(userDTO, usertype.toString());
                loadDataSet();
            }
        
    }//GEN-LAST:event_addBtnActionPerformed

