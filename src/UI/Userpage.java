    private void userTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTableMouseClicked
        int row=userTable.getSelectedRow();
        int col=userTable.getColumnCount();
        
        Object[] data=new Object[col];
        
        for(int i=1;i<col;i++){
            data[i]=userTable.getValueAt(row, i);
        }
        
        nametext.setText(data[1].toString());
        loctext.setText(data[2].toString());
        conttext.setText(data[3].toString());
        usertext.setText(data[4].toString());       
        passtext.setText(data[5].toString());
        usertype.setSelectedItem(data[6].toString());      
    }//GEN-LAST:event_userTableMouseClicked

    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        if(userTable.getSelectedRow()<0)
            JOptionPane.showConfirmDialog(null, "Please select user from table");
        else{
            int opt = JOptionPane.showConfirmDialog(
                    null,
                    "Are you sure you want to delete this product?",
                    "Confirmation",
                    JOptionPane.YES_NO_OPTION);
                if(opt==JOptionPane.YES_OPTION) {
                    new UserDAO().deleteUserDAO(
                        (String) userTable.getValueAt(
                                userTable.getSelectedRow(),0));
                    loadDataSet();
                }
        }
        
    }//GEN-LAST:event_delBtnActionPerformed

